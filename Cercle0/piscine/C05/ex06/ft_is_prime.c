int	ft_is_prime(int nb)
{
	unsigned long	n;
	unsigned long	i;

	n = nb;
	i = 5;
	if (n == 2 || n == 3)
		return (1);
	if (n < 0 || n <= 1 || n % 2 == 0 || n % 3 == 0)
		return (0);
	while (i * i <= n)
	{
		if (n % i == 0 || n % (i + 2) == 0)
			return (0);
		i += 6;
	}
	return (1);
}
