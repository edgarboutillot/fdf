int	ft_check_size(char *tab)
{
	int	i;

	i = 0;
	while (tab[i] != '\0')
	{
		if (i % 2 == 0 && (tab[i] > '0' && tab[i] <= '4'))
		{
			i++;
		}	
		else if (i % 2 == 1 && (tab[i] == 32))
		{
			i++;
		}
		else
		{
			return (0);
		}
	}
	if (i == 31)
	{
		return (1);
	}
	return (0);
}
