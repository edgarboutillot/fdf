#include <unistd.h>

void	ft_print_tab(int tab[6][6])
{
	int	i;
	int	j;

	i = 1;
	while (i < 5)
	{
		j = 1;
		while (j < 5)
		{
			write(1, &tab[i][j], 1);
			if (j != 4)
			{
				write (1, " ", 1);
			}
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}
