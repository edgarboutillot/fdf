#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (ac == 3)
	{
		while (av[2][j])
		{
			while (av[1][i])
			{
				i = 0;
				if (av[2][j] == av[1][i])
				{
					write(1, &av[1][j], 1);
				}
				i++;
			}
			j++;
		}
	}
}
